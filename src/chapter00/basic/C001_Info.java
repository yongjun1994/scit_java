package chapter00.basic;

public class C001_Info {

	// 한 줄 주석 - 한 줄 짜리 간단한 설명을 추가할 때 사용
	/*
	 * 여러 줄 주석 - 여러 줄에 걸쳐 설명을 추가할 때 사용
	 */
	/**
	 * 여러 줄 주석2 - 클래스, 메서드, 변수 등의 문서를 자동 생성하기 위한 주석
	 * @param id 사용자 아이디
	 * @return user 객체 반환
	 */
	
	public static void main(String[] args) {
		// cntl + s		저장
		// cntl + F11	실행
		// cntl + space Bar  자동완성
		System.out.println("hello world");

		
		/*
		java 프로그램을 실행하기 위해서는 main메서드가 필요
		main 메서드는 반드시 1개
		
		* 단축키
		Cntl + s 				: 저장
		Cntl + f11 				: 프로그램 실행
		Ctrl + Space 			: 입력 중에 완성 기능 호출(템플릿)
		Ctrl + D 				: 한 줄 삭제
		Ctrl + Shift + F 		: 소스 정렬
		Alt + 방향키(↑,↓) 			: 위(아래) 줄과 바꾸기
		Ctrl + Alt + Up(Down) 	: 한 줄(블럭) 복사
		Ctrl + Shift + O 		: 자동 import, 안쓰는 import 삭제
		Ctrl + S 				: 소스코드 저장 (현재 파일만)
		Ctrl + Shift + S 		: 모든 소스코드 저장
		Ctrl + /				: 주석 자동완성
		Ctrl + shift + /		: 여러줄 주석 자동완성
	*/
	}

}
